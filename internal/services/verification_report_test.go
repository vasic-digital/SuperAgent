func TestVerificationReportGenerator_SortedByScore(t *testing.T) {
	log := logrus.New()
	generator := &VerificationReportGenerator{log: log}

	entries := []ModelReportEntry{
		{ModelID: "model-1", OverallScore: 5.5, Status: "verified"},
		{ModelID: "model-2", OverallScore: 9.5, Status: "verified"},
		{ModelID: "model-3", OverallScore: 7.5, Status: "failed"},
		{ModelID: "model-4", OverallScore: 8.8, Status: "verified"},
		{ModelID: "model-5", OverallScore: 3.2, Status: "failed"},
	}

	// Sort entries as GenerateReport does before calling formatReport
	sort.Slice(entries, func(i, j int) bool {
		return entries[i].OverallScore > entries[j].OverallScore
	})

	reportContent := generator.formatReport(entries)

	// Verify models are listed in descending order by score
	// model-2 (9.5) should come before model-4 (8.8), etc.
	idx1 := findModelIndex(reportContent, "model-2")
	idx2 := findModelIndex(reportContent, "model-4")
	idx3 := findModelIndex(reportContent, "model-3")
	idx4 := findModelIndex(reportContent, "model-1")
	idx5 := findModelIndex(reportContent, "model-5")

	assert.Less(t, idx1, idx2, "model-2 (9.5) should appear before model-4 (8.8)")
	assert.Less(t, idx2, idx3, "model-4 (8.8) should appear before model-3 (7.5)")
	assert.Less(t, idx3, idx4, "model-3 (7.5) should appear before model-1 (5.5)")
	assert.Less(t, idx4, idx5, "model-1 (5.5) should appear before model-5 (3.2)")
}

func findModelIndex(content, modelID string) int {
	idx := 0
	for i := 0; i < len(content); i++ {
		if i+len(modelID) <= len(content) && content[i:i+len(modelID)] == modelID {
			idx = i
			break
		}
	}
	return idx
}
